#include <iostream>

using namespace std;

int main(){
    int n;
    cin >> n;
    long long sum = 0;
    for(int i = 0; i < n - 1; i++) {
        int x; cin >> x;
        sum += x;
    }

    long long totalSum =1LL*(1+n)*n/2;

    int missingNumber = totalSum - sum;

    cout << missingNumber;

    return 0;
}